let percentage = prompt("Please Enter Your Percentage To See Grades")
if(percentage<=49){console.log("You Are Failed")}
else if(percentage<=59){console.log("D Grade")}
else if(percentage<=69){console.log("C Garde")}
else if(percentage<=89){console.log("B Grade")}
else if(percentage<=100){console.log("A Grade")}
else{console.log("Please enter correct percentage")}